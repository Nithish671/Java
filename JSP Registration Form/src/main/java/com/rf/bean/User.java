package com.rf.bean;

public class User {
	private String name, pass, email;
	
	public void setName(String name) {
		this.name = name; 
	}
	
	public String getName() {
		return name;
	} 
	
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	public String getPass() {
		return pass;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getEmail() {
		return email;
	}
}
